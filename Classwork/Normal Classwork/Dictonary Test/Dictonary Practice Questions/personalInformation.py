# 2.	Store Personal Information:
# Create a dictionary to store a student’s personal details:
# 	•	Name: Ali
# 	•	Age: 20
# 	•	Major: Computer Science
# 	•	Print the student’s name and major.

personalDetails = {
    "Name": "Ali",
    "Age": "20",
    "Major": "Computer Science"
}

print(personalDetails.get("Name"), " + ", personalDetails.get("Major"))